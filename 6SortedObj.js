const people = [ //! Созданный заранее массив объектов который будем сортировать
  { name: 'John', age: 30 },
  { name: 'Lisa', age: 25 },
  { name: 'Mike', age: 25 },
  { name: 'Bob', age: 35 },
];

people.sort((a, b) => { //! Используя метод массива sort (мутирует исходный массив) сортируем массив на месте переменная а - первое значение для сравнение b - второе
  if (a.age === b.age) { //! Так как в условии задачи сказано что сортировать по имени если они равны пишу проверку на равны ли возраста в двух объектах по ключам age
    return a.name.localeCompare(b.name); //! Тут как и выше в функции метода строки localeCompare сравнивает 2 значение и сортирует по имени
  }
  return a.age - b.age; //! Сортируем по возрасту
});

